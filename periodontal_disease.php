<h2>Periodontal Disease</h2><p>Periodontal disease affects approximately 50% of adults age 30 and older, according to the CDC.  The first step in treatment is something called Scaling and Root Planing.  This is a treatment where the affected area is numbed, and the teeth and root surfaces are cleaned thoroughly.  This gets rid of the bacteria, plaque, and tartar, which cause the inflammation and disease.  If the disease progresses past the early stage, and more bone is lost, then more treatment needs to be done beyond the Scaling and Root Planning.  Traditionally, periodontal surgery is done to open the gums, adjust the bone, clean the root surfaces, and then suture the gums back in place. The laser now gives us an alternative to gum surgery.  The laser removes the diseased gum tissue, gives us visual access to the root surface to clean it, and stimulates the bone to heal.  It is much more comfortable than traditional gum surgery and usually doesn't result in the severe tooth sensitivity that people often experience after gum surgery.</p>



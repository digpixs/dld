<h2>Porcelain Onlay</h2><p>A porcelain onlay is a more conservative way to repair a broken tooth versus a crown.  Bonding and porcelain
technology have advanced to the point where most severely broken down teeth can be restored without having to cut the whole tooth down for a crown.  With a porcelain onlay, we only remove the decay and adjust the broken areas so that the onlay will fit over the tooth.  Then an impression is taken (just like a crown) and the lab makes a porcelain piece that is bonded to the tooth and restores it to proper form and function.  The two big advantages to an onlay over a crown are:</p>

<ol>
	<li>Much less tooth structure has to be removed to do an onlay vs a crown.  The more of your existing tooth we save, the more likely you will have that tooth for your entire life.</li>
	<li>Onlays are much kinder to your gum tissue because they usually don't go below the gumline.</li>
